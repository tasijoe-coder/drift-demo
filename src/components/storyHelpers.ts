export function inferSupplyFocus(event: NarrativeEvent, choice?: NarrativeChoice): SupplyFocus {
  const source = `${event.id} ${event.title} ${(event.text ?? []).join(' ')} ${choice?.text ?? ''}`.toLowerCase()
  const waterKeywords = ['water', 'spring', 'rain', 'drip', 'pool', 'stream', 'canteen', 'thirst', 'fresh', 'waterfall', '?', '?', '?', '?', '?', '?', '?', '?']
  const foodKeywords = ['food', 'fruit', 'crab', 'fish', 'ration', 'eat', 'meat', 'shell', 'bite', 'hunger', '?', '?', '?', '?', '??', '?', '?', '?']

  const waterScore = waterKeywords.reduce((score, keyword) => score + (source.includes(keyword) ? 1 : 0), 0)
  const foodScore = foodKeywords.reduce((score, keyword) => score + (source.includes(keyword) ? 1 : 0), 0)

  if (waterScore >= foodScore + 1) return 'water'
  if (foodScore >= waterScore + 1) return 'food'
  return 'mixed'
}

